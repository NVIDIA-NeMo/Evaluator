def _make_slurm_config(shards=None, auto_resume=False, api_key=None):
    service = {
        "type": "api",
        "url": "http://x/v1/chat/completions",
        "protocol": "chat_completions",
    }
    if api_key is not None:
        service["api_key"] = api_key

    return EvalConfig.model_validate(
        {
            "services": {
                "model": service,
            },
            "benchmarks": [
                {"name": "gsm8k", "repeats": 5, "solver": {"type": "simple", "service": "model"}},
            ],
            "cluster": {
                "type": "slurm",
                "walltime": "02:00:00",
                "auto_resume": auto_resume,
                "shards": shards,
                "node_pools": {
                    "compute": {"partition": "batch", "nodes": 1, "gres": "gpu:4"},
                },
            },
        }
    )


def _mode(path):
    return path.stat().st_mode & 0o777


class TestSbatchShardGeneration:
    def test_no_shards_no_shard_env(self):
        cfg = _make_slurm_config(shards=None)
        script, _, _ = generate_sbatch(cfg)
        assert "--array" not in script
        assert "NEL_SHARD_IDX" not in script
        assert "nel eval merge" not in script

    def test_per_shard_script_no_array(self):
        cfg = _make_slurm_config(shards=4)
        script, _, _ = generate_sbatch(cfg, shard_idx=0, total_shards=4)
        assert "--array" not in script

    def test_per_shard_exports_env_vars(self):
        cfg = _make_slurm_config(shards=4)
        script, _, _ = generate_sbatch(cfg, shard_idx=2, total_shards=4)
        assert "export NEL_SHARD_IDX=2" in script
        assert "export NEL_TOTAL_SHARDS=4" in script

    def test_per_shard_output_dir(self):
        cfg = _make_slurm_config(shards=4)
        script, _, _ = generate_sbatch(cfg, shard_idx=1, total_shards=4)
        assert "/shard_1" in script

    def test_per_shard_report_only_after_merge(self):
        """Reports are generated by the last shard after merge, not per-shard."""
        cfg = _make_slurm_config(shards=4)
        script, _, _ = generate_sbatch(cfg, shard_idx=0, total_shards=4)
        merge_pos = script.index("nel eval merge")
        report_pos = script.index("nel eval report")
        assert report_pos > merge_pos
        assert (
            "_PARENT_DIR"
            in script.splitlines()[next(idx for idx, ln in enumerate(script.splitlines()) if "nel eval report" in ln)]
        )

    def test_per_shard_has_auto_resume(self):
        """Each shard is an independent job with its own auto_resume chain."""
        cfg = _make_slurm_config(shards=4, auto_resume=True)
        script, _, _ = generate_sbatch(cfg, shard_idx=0, total_shards=4)
        assert "afternotok" in script
        assert "sacct" in script

    def test_per_shard_includes_auto_merge(self):
        cfg = _make_slurm_config(shards=4)
        script, _, _ = generate_sbatch(cfg, shard_idx=0, total_shards=4)
        assert "nel eval merge" in script
        assert ".shard_done" in script
        assert ".merge_lock" in script
        assert "All 4 shards complete" in script

    def test_shard_done_only_on_success(self):
        """Completion marker is only written when NEL_EXIT_CODE is 0."""
        cfg = _make_slurm_config(shards=2)
        script, _, _ = generate_sbatch(cfg, shard_idx=0, total_shards=2)
        assert "if [[ $NEL_EXIT_CODE -eq 0 ]]" in script

    def test_stale_shard_done_cleared_on_start(self):
        """Old .shard_done marker is removed at shard start to prevent stale counts."""
        cfg = _make_slurm_config(shards=3)
        script, _, _ = generate_sbatch(cfg, shard_idx=1, total_shards=3)
        assert 'rm -f "$OUTPUT_DIR/.shard_done"' in script
        rm_pos = script.index('rm -f "$OUTPUT_DIR/.shard_done"')
        touch_pos = script.index('touch "$OUTPUT_DIR/.shard_done"')
        assert rm_pos < touch_pos

    def test_merge_failure_removes_lock(self):
        """Failed merge removes .merge_lock so retries can succeed."""
        cfg = _make_slurm_config(shards=2)
        script, _, _ = generate_sbatch(cfg, shard_idx=0, total_shards=2)
        assert "_MERGE_RC=$?" in script
        assert "rm -rf" in script
        assert ".merge_lock/.done" in script

    def test_merge_success_writes_done_sentinel(self):
        """Successful merge creates .merge_lock/.done sentinel."""
        cfg = _make_slurm_config(shards=2)
        script, _, _ = generate_sbatch(cfg, shard_idx=0, total_shards=2)
        assert 'touch "$_PARENT_DIR/.merge_lock/.done"' in script

    def test_stale_merge_lock_cleaned_before_retry(self):
        """Stale lock (without .done) is removed before mkdir attempt."""
        cfg = _make_slurm_config(shards=2)
        script, _, _ = generate_sbatch(cfg, shard_idx=0, total_shards=2)
        assert '! -f "$_PARENT_DIR/.merge_lock/.done"' in script

    def test_shard_count_uses_stat_not_ls(self):
        """Shard counting uses per-path stat loop, not ls glob (Lustre reliability)."""
        cfg = _make_slurm_config(shards=3)
        script, _, _ = generate_sbatch(cfg, shard_idx=0, total_shards=3)
        assert "ls -1d" not in script
        assert "for _s in $(seq 0" in script
        assert '-f "$_PARENT_DIR/shard_$_s/.shard_done"' in script

    def test_shards_1_edge_case(self):
        """shards=1 produces a single shard script that self-merges."""
        cfg = _make_slurm_config(shards=1)
        script, _, _ = generate_sbatch(cfg, shard_idx=0, total_shards=1)
        assert "export NEL_SHARD_IDX=0" in script
        assert "export NEL_TOTAL_SHARDS=1" in script
        assert "All 1 shards complete" in script

    def test_per_shard_always_has_resume_flag(self):
        """--resume is always passed for shard scripts even without auto_resume."""
        cfg = _make_slurm_config(shards=4, auto_resume=False)
        script, _, _ = generate_sbatch(cfg, shard_idx=0, total_shards=4)
        assert "--resume" in script

    def test_direct_generate_with_shards_rejected(self):
        """Calling generate_sbatch without shard_idx when shards is set should fail."""
        import pytest

        cfg = _make_slurm_config(shards=4)
        with pytest.raises(ValueError, match="write_sbatch"):
            generate_sbatch(cfg)

    def test_no_shards_has_report(self):
        cfg = _make_slurm_config(shards=None)
        script, _, _ = generate_sbatch(cfg)
        assert "nel eval report" in script


